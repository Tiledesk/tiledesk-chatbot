class TiledeskMath {
    static cos(x) {
        return Math.cos(x);
    }

    static sin(x) {
        return Math.sin(x);
    }

    static tan(x) {
        return Math.tan(x);
    }

    static abs(x) {
        return Math.abs(x);
    }

    static ceil(x) {
        return Math.ceil(x);
    }

    static floor(x) {
        return Math.floor(x);
    }

    static round(x) {
        return Math.round(x);
    }
}

module.exports = { TiledeskMath };